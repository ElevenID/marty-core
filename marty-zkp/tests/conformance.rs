/// Verifier must reject a proof generated with a deliberately wrong value for
/// `birth_date` (the CBOR encoding of "2000-01-01" instead of "1971-09-01").
///
/// The ZK mock does not validate attribute values, so this test is only
/// meaningful with the real Longfellow library.
#[test]
#[cfg_attr(zk_mock, ignore)]
fn prove_rejects_wrong_birth_date_witness() {
    // CBOR tag(1004) tstr "2000-01-01" — wrong value, not in the mDoc
    const WRONG_DATE: &[u8] = &[
        0xd9, 0x03, 0xec, 0x6a, b'2', b'0', b'0', b'0', b'-', b'0', b'1', b'-', b'0', b'1',
    ];

    let circuit = Circuit::generate(2).expect("generate 2-attr circuit");
    let input = test_input_3(vec![
        AttributeRequest::new("org.iso.18013.5.1", "age_over_18", vec![0xf5]),
        AttributeRequest::new("org.iso.18013.5.1", "birth_date", WRONG_DATE.to_vec()),
    ]);

    // The real library must either reject at prove-time or produce a proof
    // that the verifier rejects.  Either is acceptable.
    let outcome = Prover::prove(&circuit, &input)
        .and_then(|proof| Verifier::verify(&circuit, &input, &proof));
    match outcome {
        Ok(false) | Err(_) => {} // correct: wrong witness rejected
        Ok(true) => panic!("wrong birth_date witness must not produce a valid proof"),
    }
}

